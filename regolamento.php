			<h2>Somma Gol</h2>
			
			<p>Coppa Narciso e Zenit Champions Cup sono due competizioni a somma gol , dove il punteggio totale della giornata per ogni partecipante viene convertito in “gol”.</p>
			
			<p>Per farlo verrà utilizzata la seguente tabella di conversione:
			· fino a 65,5 punti = 0 gol;
			· da 66 a 71,5 punti = 1 gol;
			· da 72 a 77,5 punti = 2 gol;
			· da 78 a 83,5 punti = 3 gol;
			· da 84 a 89, 5 punti = 4 gol;
			· da 90 a 95,5 punti = 5 gol;
			· da 96 a 101,5 punti = 6 gol;
			· e così via …</p>
			
			<p>Ogni squadra segnerà così un certo numero di gol che decideranno il risultato della partita: sconfitta = 0 punti in classifica; pareggio = 1 punto in classifica; vittoria = 3 punti in classifica.</p>
			
			<p>In ciascuna delle fasi, tra le squadre che avranno raggiunto il medesimo piazzamento verranno considerate come migliori (e quindi avranno la precedenza) le squadre con il punteggio più alto totalizzato, la miglior differenza reti e poi quelle che hanno segnato il maggior numero di gol.</p>
			
			<h2>Parità</h2>
			
			<p>Distinguendo due tipi di classifiche, quella a somma punti semplice che può verificarsi nel campionato Zenit e quella che può verificarsi all’interno di un girone all’italiana o in uno scontro diretto.</p>
			
			<p>Per quanto riguarda il campionato Zenit, può verificarsi la possibilità che alla 19^ giornata, giornata in cui si devono delineare i gironi di Champions, due o più squadre si trovino con lo stesso punteggio; in tal caso si andrà ad effettuare uno scontro diretto tra di esse se il calendario ne permette lo svolgimento, e nel caso di ulteriore parità si effettuerà un sorteggio.
			
			Nel caso in cui il calendario non permetta lo spareggio si procederà direttamente all’ effettuazione di un sorteggio.</p>
			
			<p>In caso di parità invece in classifica tra 2 o più squadre all’interno di un determinato girone si andrà a considerare la somma punti del girone, se anche essa è uguale, si procederà come nel caso precedente, andando a svolgere se possibile uno spareggio alla giornata successiva, altrimenti si applicherà un sorteggio.</p>
			
			<p>Nelle situazioni di passaggio del turno tra le migliori squadre tra tutti i gironi si considera prima il punteggio del girone, e successivamente la somma punti totale del girone, e si stabilisce una graduatoria. Se sussistono ancora situazioni di parità si svolgeranno degli spareggi, a somma punti o scontri diretti, la cui formula sarà decisa caso per caso (dipende dal numero di squadre).
			
			Nel caso in cui non sia possibile effettuare gli spareggi si procederà ad un sorteggio.</p>
			
			<h2>Handicap</h2>
			
			<p>Le squadre che non inseriscono la formazione per più di tre giornate consecutivamente, avranno una penalizzazione di 6 punti nello scontro diretto.</p>
			
			<p>Alla quinta giornata senza formazione sarà assegnato un punteggio d’ufficio uguale a 0 nelle competizioni a somma punti, in quelle a somma gol il partecipante perderà la partita 3-0 a tavolino.</p>
			
			<p>Nelle competizioni a somma punti la mancanza reiterata dell’inserimento della formazione, specialmente nelle fasi finali dei tornei può portare all’esclusione dalla classifica finale.</p>
			
