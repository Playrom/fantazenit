			<p>Tutte le competizioni saranno svolte in contemporanea.</p>
			
			<h3>Campionato Zenit</h3>
			
			<p>Tutte le squadre partecipanti  al torneo prenderanno parte d’ufficio al girone unico del “Campionato Zenit”.</p>
			
			<p>La classifica sarà stilata sommando i punteggi ottenuti in ogni giornata da ogni squadra , include penalizzazioni e bonus di punteggio.</p>
			
			<p>Risulterà vincitore il partecipante con il più alto punteggio al termine della 38° giornata.</p>
			
			<h3>Zenit Champions Cup</h3>
			
			<p>Al termine della 19° giornata del torneo verrà stilata una classifica provvisoria per il “Campionato Zenit”.</p>
			
			<p>Le 32 squadre meglio piazzate in questa classifica ,attraverso un sistema di teste di serie potranno accedere alla fase finale di questa competizione che prevede una fase a gironi e turni a eliminazione diretta con scontri di andata e ritorno.
			
			Le prime 32 squadre della classifica generale vengono divise in 4 fasce, e sorteggiate in 8 gironi da 4 con partite di andata e ritorno. Le prime due squadre per ogni turno accederanno al tabellone Finale con Ottavi, Quarti, Semifinali e Finale.</p>
			
			<h3>Coppa Narciso</h3>
			
			<p>Alla Coppa Narciso, partecipano d’ufficio tutte le squadre iscritte al Fanta Zenit.
			Il Calendario ed le Modalità sanno rese note ad iscrizione completate</p>
			
			<h3>Campionatino</h3>
			
			<p>Al termine della 7° giornata, sulla base della classifica generale che si sarà determinata, i partecipanti verranno suddivisi in 3 serie: Seria A, Serie B, e Serie C.</p>
			
			<p>Nelle successive 10 giornate verrà conteggiato i punti fatti da ogni partecipante, determinando così le 3 nuove classifiche (una per ogni serie).</p>
			
