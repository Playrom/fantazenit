			<p>Contemporaneamente un certo ugual numero di squadre ( da determinare a chiusura iscrizioni ) retrocederanno l dalla Serie A alla Serie B, così come altri dalla Serie B alla Serie C, mentre altri verranno promossi in Serie A ed in Serie B.</p>
			
			<p>Le classifiche dei partecipanti delle tre serie vengono determinate con la somma punti,in caso di arrivo a parità il premio verrà diviso e salirà o scenderà nell’altra categoria la squadra meglio piazzata in quel momento in classifica Generale del Campionato Zenit.</p>
			
