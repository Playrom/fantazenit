			<h2>Punti</h2>
			
			<p>Al termine di ogni giornata di Campionato, ai singoli calciatori della Serie A viene attribuito un punteggio, utilizzando le liste fornite da “Fantagazzetta”, relative alla redazione di  Milano.
			
			Pertanto ogni squadra iscritta al Fanta Zenit 2015/2016 in ogni giornata del torneo ottiene tanti punti quanti ne conquistano gli 11 calciatori reali “scesi in campo” nella relativa giornata di Serie A.</p>
			
			<p>I calciatori che contribuiscono ad aumentare i  punti del partecipante sono solo i titolari che hanno ottenuto punteggio ai fini del gioco e gli eventuali calciatori della panchina che hanno sostituito quei titolari che non hanno ricevuto punteggio (per un massimo di tre sostituzioni).
			
			Il computer, calcolando il punteggio di ogni squadra, sostituirà utilizzando dalla panchina un massimo di tre calciatori ed inserendoli al posto di chi non ha preso punti, seguendo l’ordine di inserimento in panchina, e solo nel caso in cui il ruolo del subentrate e del sostituto sono identici.</p>
			
			<p>A questo punteggio di somma voti possono sommarsi dei bonus e sottrarsi dei malus ottenuti da ognuno dei calciatori</p>
			
			<h3>bonus</h3>
			
			<ul>
			<li>3 punti per ogni gol segnato</li>
			<li>1 per ogni assist servito</li>
			<li>3 punti per ogni rigore parato</li>
			</ul>
			
			
			<h3>malus</h3>
			
			<ul>
			<li>-0,5 punto per un’ammonizione</li>
			<li>-1 punto per una espulsione</li>
			<li>-1 punto per ogni gol subito dal portiere</li>
			<li>-2 punti per ogni autogol effettuato</li>
			<li>-3 punti per ogni rigore sbagliato</li>
			</ul>
			
			
			<p>Le liste bonus e malus sono fornite da fantagazzetta.</p>
			
			<p>Ogni qual volta un calciatore non riceva un voto in pagella si procederà ad effettuare le sostituzioni dalla panchina, come da regolamento.</p>
			
			<p>In casi particolari, il calciatore senza voto riceve comunque un voto d’ufficio:</p>
			
			<p>A) Calciatori (escluso portiere):</p>
			
			<ul>
			<li>il calciatore s.v. che ottiene un bonus riceve 6 punti d’ufficio (a cui vanno aggiunti i relativi bonus) indipendentemente dal tempo giocato;</li>
			<li>il calciatore s.v. che viene espulso prende 4 punti d’ufficio (comprensivi del malus per l’espulsione);</li>
			<li>il calciatore s.v che sbaglia un rigore o che fa un’autorete riceve 6 punti d’ufficio (a cui vanno sottratti i relativi malus);
			
			Il calciatore s.v. senza bonus o malus, così come il calciatore s.v. che viene ammonito, non viene conteggiato, cioè è come se non fosse sceso in campo.</li>
			</ul>
			
			
			<p>B) Portiere:</p>
			
			<ul>
			<li>il portiere s. v. sceso in campo (anche se poi sostituito) riceve in ogni caso 6 punti d’ufficio (a cui vanno sottratti eventuali malus ed aggiunti eventuali bonus) se ha giocato per almeno 25′ (esclusi i minuti di recupero)</li>
			<li>il portiere s.v. che non ha giocato per almeno 25′ (esclusi i minuti di recupero) non viene conteggiato, cioè è come se non fosse sceso in campo a meno che non abbia ricevuto bonus o malus. In tal caso:</li>
			<li>se come malus ha subito la sola ammonizione e non ha preso bonus, non viene conteggiato;</li>
			<li> per ogni altro tipo di bonus/malus gli vengono assegnati, come ad ogni altro giocatore, 6 punti d’ufficio, a cui vanno aggiunti i bonus e i malus (ai quali si va ad aggiungere a quel punto anche un’eventuale ammonizione)</li>
			<li>  in caso di espulsione, il punteggio d’ufficio sarà 4, comprensivo del malus per l’espulsione (e non 6)</li>
			</ul>
			
			
			<h2>Modificatore di Difesa</h2>
			
			<p>Importante variazione del punteggio riguarda il modificatore di difesa.
			
			Il modificatore della difesa è un bonus che si calcola solo se il portiere e almeno quattro difensori contribuiscono ad aumentare il punteggio della squadra. Quindi vengono escluse d’ufficio dal calcolo  del bonus tutte le formazioni che utilizzano un modulo con 3 giocatori in difesa.
			
			
			Si considerano il voto in pagella del portiere e i tre migliori voti in pagella ottenuti dai difensori (non vanno considerati i bonus e i malus). Nel caso dei senza voto che ricevono punteggio ai fini del gioco (vedi punto precedente) a tal punteggio si sottraggono i bonus e si aggiungono i malus.
			
			Si calcola la media di questi 4 valori e si da questa media si ottengono questi bonus da aggiungere al punteggio totale della squadra:</p>
			
			<ul>
			<li>6 punti se la media è maggiore o uguale a 7</li>
			<li>3 punti se la media è maggiore o uguale a 6,5 e minore di 7</li>
			<li>1 punto se la media è maggiore o uguale a 6 e minore di 6,5</li>
			</ul>
			
			
